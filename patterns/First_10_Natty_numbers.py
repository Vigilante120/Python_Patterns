goal = 10
for i in range(1, goal + 1):
    print(i)

"""goal = 10: This sets the upper limit for our sequence of numbers.

for i in range(1, goal + 1):: This is a for loop that iterates from 1 to 10.

range(1, goal + 1) creates a sequence from 1 to 10 (inclusive).

We use goal + 1 because range() excludes the upper limit.

print(i): This prints the current value of i in each iteration."""